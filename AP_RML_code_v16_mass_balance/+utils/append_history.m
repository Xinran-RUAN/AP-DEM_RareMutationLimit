function val = get_field(s, name, defaultValue)
val = defaultValue;
if isstruct(s) && isfield(s, name) && ~isempty(s.(name)) && (isnumeric(s.(name)) || islogical(s.(name))) && isscalar(s.(name))
    val = double(s.(name));
end
end
